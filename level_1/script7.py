# some conversions 

i = 111

print (" i is of type " , type(i))

j = str(i)

print (" j is of type " , type(j))

k = "999"
print (" k is of type " , type(k))

l = int(k)

print (" l is of type " , type(l))

print(hex(int(k)))

print(oct(int(k)))

print(bin(int(k)))

string = "blackberry"

print(list(string))

print(set(string))

print(tuple(string))

